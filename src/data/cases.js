// ある評価項目IDに対応する取り組みを持つ事例を取得
export function getCasesByItemId(itemId) {
  return CASES.filter((c) =>
    c.initiatives.some((init) => init.itemId === itemId)
  );
}

// 選択した評価項目ID群すべて(AND) or いずれか(OR) を満たす事例を取得
// mode: "and" | "or"
export function searchCases(itemIds, mode = "or") {
  if (!itemIds || itemIds.length === 0) return CASES;
  if (mode === "and") {
    return CASES.filter((c) => {
      const covered = new Set(c.initiatives.map((i) => i.itemId));
      return itemIds.every((id) => covered.has(id));
    });
  }
  return CASES.filter((c) =>
    c.initiatives.some((i) => itemIds.includes(i.itemId))
  );
}

// 事例の取り組みのうち、フロム・シェフでサポート可能な項目IDを取得
export function getSupportableInitiatives(caseData, supportableItemIds) {
  return caseData.initiatives.filter((init) =>
    supportableItemIds.includes(init.itemId)
  );
}

